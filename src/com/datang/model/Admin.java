package com.datang.model;

public class Admin {
	private String acc_number;
	
	private String acc_pwd;
	private String phone;
	private String mibao_Q;
	private String mibao;
	public String getAcc_number() {
		return acc_number;
	}
	public void setAcc_number(String acc_number) {
		this.acc_number = acc_number;
	}
	public String getAcc_pwd() {
		return acc_pwd;
	}
	public void setAcc_pwd(String acc_pwd) {
		this.acc_pwd = acc_pwd;
	}
	public String getPhone() {
		return phone;
	}
	public void setPhone(String phone) {
		this.phone = phone;
	}
	public String getMibao_Q() {
		return mibao_Q;
	}
	public void setMibao_Q(String mibao_Q) {
		this.mibao_Q = mibao_Q;
	}
	public String getMibao() {
		return mibao;
	}
	public void setMibao(String mibao) {
		this.mibao = mibao;
	}
	
}
